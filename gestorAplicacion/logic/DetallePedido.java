package gestorAplicacion.logic;

public class DetallePedido {
	
	private Pedido pedidoD;
	private int cantidad;
	private Comida comidaD;
	private int precioD;
	
	public Pedido getPedidoD() {
		return pedidoD;
	}
	public void setPedidoD(Pedido pedidoD) {
		this.pedidoD = pedidoD;
	}
	public int getCantidad() {
		return cantidad;
	}
	public void setCantidad(int cantidad) {
		this.cantidad = cantidad;
	}
	public Comida getComidaD() {
		return comidaD;
	}
	public void setComidaD(Comida comidaD) {
		this.comidaD = comidaD;
	}
	public int getPrecioD() {
		return precioD;
	}
	public void setPrecioD(int precioD) {
		this.precioD = precioD;
	}
	
	
	
	
}
